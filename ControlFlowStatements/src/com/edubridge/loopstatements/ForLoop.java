package com.edubridge.loopstatements;

import java.util.Scanner;

public class ForLoop {
	   @SuppressWarnings("resource")
	public static void main(String[] args) {
		      Scanner s = new Scanner(System.in);
		      int n = s.nextInt();

		      for(byte i = 1; i <= n; --n) {
		         System.out.print(n + " ");
		      }

		   }
		}
		    
